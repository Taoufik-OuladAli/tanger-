            <section>
                <h2>Le cadre climatique</h2>
                <p>
                    La région de Tanger présente un climat subhumide grâce à ses deux façades maritimes : 
                    doux et humides. La région connait une pluviométrie moyenne de 800 mm/an. 
                    Les températures restent clémentes en hiver, douces en été. Elles atteignent rarement 0 °C 
                    au mois de janvier et les maxima les plus fréquents de ce mois oscillent entre 14 °C et 18 °C. 
                    En été, l’atmosphère se réchauffe sensiblement, les températures maxima les plus fréquentes 
                    en juillet varient de 16 °C à 26 °C. Des pics de 32 °C à 38 °C ont pu être enregistres quelques 
                    jours par an, mais leur fréquence demeure exceptionnellement.
