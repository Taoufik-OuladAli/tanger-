                <p>
                    La région est caractérise par d’importantes variabilités topographiques 
                    (altitudes élevés pour les montagnes du rif et basses au niveau des plaines côtières..), 
                    climatiques (montagnes très arrosée et plaines orientales semi arides) et géologiques 
                    (nappes karstiques très importantes, zones étendues sans aquifères, plaines et vallées 
                    réduites).
                    La province de Tanger est située au Nord-ouest du pays elle est délimitée par la mer
                    Méditerranée et par le détroit de Gibraltar au Nord, au sud il y a la province de Larache, 
                    a l'Est celle de Tétouan et a l’Ouest elle est bordée par l’océan atlantique, la superficie
                    de la province de Tanger est de 1 195 km2 (dont 332 km2 pour la préfecture d’El Fahs-béni 
                    makada crée en avril 1997)2.
