                        Maroc, entre 1923 et 1956, avec un intermède durant la Seconde Guerre mondiale. 
                        Bien que demeurant sous la souveraineté du sultan du Maroc, la zone internationale de 
                        Tanger était sous administration conjointe de la France, de l'Espagne, du Royaume-Uni, 
                        du Portugal, de l'Italie, de la Belgique, des Pays-Bas, de la Suède et des États-Unis. 
                        Le sultan était représenté par un mendoub, qui faisait fonctions de pacha et présidait 
                        l'Assemblée internationale. Des tribunaux mixtes rendaient la justice. 
                        En 1940, l'Espagne franquiste occupe Tanger et la réunit au territoire du Rif. Madrid 
                        retire toutefois ses troupes en mai 1944 et la ville retrouve son autonomie, jusqu'à sa 
                        réunion au Maroc en 1956.
